#include "cranium.h"

char*	delete_last_char(char **s)
{
    int i;

    i = 0;
    while ((*s)[i] != '\0')
        i++;
    (*s)[i - 1] = '\0';
    return (*s);
}

char*	delete_begin(char **s)
{
    int i;
    int j;

    i = 0;
    j = 4;
    while ((*s)[j] != '\0')
    	(*s)[i++] = (*s)[j++];
    (*s)[i] = '\0';
    return (*s);
}